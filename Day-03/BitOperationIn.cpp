#include <bits/stdc++.h>
#include <iostream>
using namespace std;
int main()
{
    // bit wish operator <<
    int a = 11;
    cout << (a << 1) << endl;
    // bit wish operator >>
    int s = 2;
    cout << (s >> 1) << endl;
    // bitwish operator &
    int as = 4;
    cout << (as & as);
    // bit wish operator |
    // bit wish operator ^
    // bit wish operator ~
    return 0;
}