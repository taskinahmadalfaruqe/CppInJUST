#include <bits/stdc++.h>
#include <iostream>
using namespace std;

int main()
{
    // if statement
    int a, b;
    cin >> a >> b;
    if (a > b)
    {
        cout << "It's True.";
    }
    else if (a == b)
    {
        cout << "It's Equal.";
       
    }

    else
    {
        cout << "It's False.";
    }

    return 0;
}